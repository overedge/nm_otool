# include "nm.h"

uint32_t little_to_big_32(uint32_t a)
{
	uint32_t	b;

	b = ((a>>24)&0xff) |
		((a<<8)&0xff0000) |
		((a>>8)&0xff00) |
		((a<<24)&0xff000000);
	return (b);
}
