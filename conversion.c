# include "nm.h"

uint32_t little_to_big_32_uint(uint32_t a)
{
	uint32_t	b;

	b = ((a>>24)&0xff) |
		((a<<8)&0xff0000) |
		((a>>8)&0xff00) |
		((a<<24)&0xff000000);
	return (b);
} 

int little_to_big_32_int(int a)
{
	int		b;

	b = ((a << 8) & 0xFF00FF00) | ((a >> 8) & 0xFF00FF ); 
	return (a << 16) | ((a >> 16) & 0xFFFF);
}
